package kg.toilink.service;

import com.fasterxml.jackson.databind.JsonNode;
import com.fasterxml.jackson.databind.ObjectMapper;
import kg.toilink.dto.request.RsvpRequest;
import kg.toilink.entity.Event;
import kg.toilink.entity.Guest;
import kg.toilink.entity.RsvpResponse;
import kg.toilink.exception.BadRequestException;
import kg.toilink.exception.NotFoundException;
import kg.toilink.repository.EventRepository;
import kg.toilink.repository.GuestRepository;
import kg.toilink.repository.RsvpResponseRepository;
import kg.toilink.util.PhoneUtils;
import lombok.RequiredArgsConstructor;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import java.time.LocalDateTime;
import java.util.Objects;
import java.util.Optional;
import java.util.Set;
import java.util.UUID;

@Service
@RequiredArgsConstructor
public class RsvpService {

    private static final Set<String> RSVP_STATUSES = Set.of("ATTENDING", "DECLINED", "MAYBE");
    private static final int MAX_GROUP_SIZE = 20;

    private final EventRepository eventRepository;
    private final GuestRepository guestRepository;
    private final RsvpResponseRepository rsvpResponseRepository;
    private final ObjectMapper objectMapper = new ObjectMapper();

    @Transactional
    public RsvpResult rsvp(String slug, RsvpRequest req) {
        Event event = eventRepository.findBySlug(slug)
                .orElseThrow(() -> NotFoundException.eventBySlug(slug));

        validateStatus(req.status());
        if ("DRAFT".equals(event.getStatus())) {
            throw new BadRequestException("Event is not published yet");
        }
        if ("CLOSED".equals(event.getStatus())) {
            throw new BadRequestException("Event is closed for responses");
        }
        if (event.getRsvpDeadline() != null && LocalDateTime.now().isAfter(event.getRsvpDeadline())) {
            throw new BadRequestException("RSVP deadline has passed");
        }

        String groupCode = validateGroupCode(event, req.groupCode());
        int groupSize = normalizeGroupSize(req.groupSize());
        Guest guest = ensureGuestToken(resolveGuest(event, req, groupCode));

        // Spouse companion: only when event explicitly allows it (blocksConfig.rsvp.allowCompanion === true)
        if (isCompanionAllowed(event)) {
            manageSpouseCompanion(guest, event, req.spouseName(), req.status());
        }

        rsvpResponseRepository.findByGuestIdAndEventId(guest.getId(), event.getId())
                .ifPresentOrElse(
                        existing -> {
                            existing.setStatus(req.status());
                            existing.setGroupSize(groupSize);
                            existing.setComment(req.comment());
                            rsvpResponseRepository.save(existing);
                        },
                        () -> rsvpResponseRepository.save(RsvpResponse.builder()
                                .guest(guest)
                                .event(event)
                                .status(req.status())
                                .groupSize(groupSize)
                                .comment(req.comment())
                                .build())
                );

        return new RsvpResult(req.status(), guest.getToken());
    }

    /** Result returned to controller — includes guestToken for localStorage persistence */
    public record RsvpResult(String status, java.util.UUID guestToken) {}

    private Guest resolveGuest(Event event, RsvpRequest req, String groupCode) {
        // 1. Token-based match (most precise — guest opens a personal link or returns via localStorage token)
        if (req.guestToken() != null) {
            Guest guest = guestRepository.findByTokenAndDeletedAtIsNull(req.guestToken())
                    .orElseThrow(() -> new BadRequestException("Invalid guest token"));
            if (!guest.getEvent().getId().equals(event.getId())) {
                throw new BadRequestException("Guest token does not belong to this event");
            }
            return applyIncomingFields(guest, req, groupCode);
        }

        if (req.name() == null || req.name().isBlank()) {
            throw new BadRequestException("Name is required when responding through the public link");
        }

        // 2. Phone-based dedup — find existing guest with same normalized phone in this event
        String normalizedPhone = PhoneUtils.normalize(req.phone());
        if (normalizedPhone != null) {
            Optional<Guest> match = findByNormalizedPhone(event.getId(), normalizedPhone);
            if (match.isPresent()) {
                return applyIncomingFields(match.get(), req, groupCode);
            }
        }

        // 3. Create new guest — token auto-generated by @PrePersist; group code from validated URL.
        return guestRepository.save(Guest.builder()
                .event(event)
                .name(req.name().trim())
                .phone(req.phone() != null && !req.phone().isBlank() ? req.phone().trim() : null)
                .source("PUBLIC_LINK")
                .side(groupCode != null ? groupCode : null)
                .token(UUID.randomUUID())
                .build());
    }

    /**
     * Update an existing guest with incoming RSVP fields.
     * Name is overwritten if non-blank. Phone is filled if missing.
     * Side is overwritten when the URL carries a specific groupCode — we trust the URL.
     * Source is never downgraded (PERSONAL_LINK stays PERSONAL_LINK even if guest opens public link).
     */
    private Guest applyIncomingFields(Guest guest, RsvpRequest req, String groupCode) {
        boolean changed = false;
        if (req.name() != null && !req.name().isBlank()) {
            guest.setName(req.name().trim());
            changed = true;
        }
        if (req.phone() != null && !req.phone().isBlank()
                && (guest.getPhone() == null || guest.getPhone().isBlank())) {
            guest.setPhone(req.phone().trim());
            changed = true;
        }
        if (req.groupCode() != null && !req.groupCode().isBlank()
                && !Objects.equals(groupCode, guest.getSide())) {
            guest.setSide(groupCode);
            changed = true;
        }
        return changed ? guestRepository.save(guest) : guest;
    }

    private Guest ensureGuestToken(Guest guest) {
        if (guest.getToken() != null) {
            return guest;
        }
        guest.setToken(UUID.randomUUID());
        return guestRepository.save(guest);
    }

    private void validateStatus(String status) {
        if (status == null || !RSVP_STATUSES.contains(status)) {
            throw new BadRequestException("Status must be ATTENDING, DECLINED or MAYBE");
        }
    }

    private int normalizeGroupSize(Integer groupSize) {
        if (groupSize == null) return 1;
        if (groupSize < 1 || groupSize > MAX_GROUP_SIZE) {
            throw new BadRequestException("Group size must be between 1 and " + MAX_GROUP_SIZE);
        }
        return groupSize;
    }

    private String validateGroupCode(Event event, String groupCode) {
        if (groupCode == null || groupCode.isBlank()) {
            return null;
        }
        String trimmed = groupCode.trim();
        String guestGroups = event.getGuestGroups();
        if (guestGroups == null || guestGroups.isBlank()) {
            throw new BadRequestException("Unknown guest group");
        }
        try {
            JsonNode groups = objectMapper.readTree(guestGroups);
            if (!groups.isArray()) {
                throw new BadRequestException("Unknown guest group");
            }
            for (JsonNode group : groups) {
                JsonNode code = group.get("code");
                if (code != null && trimmed.equals(code.asText())) {
                    return trimmed;
                }
            }
        } catch (BadRequestException e) {
            throw e;
        } catch (Exception e) {
            throw new BadRequestException("Unknown guest group");
        }
        throw new BadRequestException("Unknown guest group");
    }

    private Optional<Guest> findByNormalizedPhone(Long eventId, String normalizedPhone) {
        return guestRepository.findActiveByEventIdAndPhoneNormalized(eventId, normalizedPhone);
    }

    /** Reads event.blocksConfig.rsvp.allowCompanion (boolean). Default: false. */
    private boolean isCompanionAllowed(Event event) {
        String blocksConfig = event.getBlocksConfig();
        if (blocksConfig == null || blocksConfig.isBlank()) return false;
        try {
            JsonNode root = objectMapper.readTree(blocksConfig);
            JsonNode flag = root.path("rsvp").path("allowCompanion");
            return flag.isBoolean() && flag.asBoolean();
        } catch (Exception e) {
            return false;
        }
    }

    /**
     * Manage the auto-created spouse companion for a primary guest.
     * - spouseName non-blank → create or update auto-companion (source = COMPANION_AUTO).
     * - spouseName blank/null → delete existing auto-companion (manual companions added by organizer are untouched).
     * Companion inherits side from primary.
     */
    private void manageSpouseCompanion(Guest primary, Event event, String spouseName, String status) {
        Guest existingAuto = findAutoCompanion(primary);

        if (spouseName != null && !spouseName.isBlank()) {
            String trimmed = spouseName.trim();
            if (existingAuto != null) {
                if (!Objects.equals(existingAuto.getName(), trimmed)
                        || !Objects.equals(existingAuto.getSide(), primary.getSide())) {
                    existingAuto.setName(trimmed);
                    existingAuto.setSide(primary.getSide());
                    guestRepository.save(existingAuto);
                }
                syncCompanionRsvp(existingAuto, event, status);
                return;
            }
            Guest companion = guestRepository.save(Guest.builder()
                    .event(event)
                    .name(trimmed)
                    .source("COMPANION_AUTO")
                    .side(primary.getSide())
                    .relatedToId(primary.getId())
                    .relationType("SPOUSE")
                    .build());
            syncCompanionRsvp(companion, event, status);
            primary.setRelatedToId(companion.getId());
            primary.setRelationType("SPOUSE");
            guestRepository.save(primary);
            return;
        }

        // spouseName blank → remove auto-companion if it exists; manual companions are kept.
        if (existingAuto != null) {
            if (Objects.equals(primary.getRelatedToId(), existingAuto.getId())) {
                primary.setRelatedToId(null);
                primary.setRelationType(null);
                guestRepository.save(primary);
            }
            rsvpResponseRepository.findByGuestIdAndEventId(existingAuto.getId(), event.getId())
                    .ifPresent(rsvpResponseRepository::delete);
            existingAuto.setDeletedAt(LocalDateTime.now());
            guestRepository.save(existingAuto);
        }
    }

    private void syncCompanionRsvp(Guest companion, Event event, String status) {
        if (status == null || status.isBlank()) return;
        rsvpResponseRepository.findByGuestIdAndEventId(companion.getId(), event.getId())
                .ifPresentOrElse(
                        existing -> {
                            existing.setStatus(status);
                            existing.setGroupSize(1);
                            rsvpResponseRepository.save(existing);
                        },
                        () -> rsvpResponseRepository.save(RsvpResponse.builder()
                                .guest(companion)
                                .event(event)
                                .status(status)
                                .groupSize(1)
                                .build())
                );
    }

    /** Returns the auto-created companion linked to the primary guest, if any. */
    private Guest findAutoCompanion(Guest primary) {
        if (primary.getRelatedToId() != null) {
            Optional<Guest> related = guestRepository.findById(primary.getRelatedToId());
            if (related.isPresent() && "COMPANION_AUTO".equals(related.get().getSource())
                    && related.get().getDeletedAt() == null) {
                return related.get();
            }
        }
        return guestRepository.findBySourceAndRelatedToIdAndDeletedAtIsNull("COMPANION_AUTO", primary.getId())
                .orElse(null);
    }
}
